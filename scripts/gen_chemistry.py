def build():
    points = []
    for z, symbol, name, formula, category, chapter, riddle, explanation, use in ELEMENTS:
        points.append({
            "id": f"el-{symbol.lower()}",
            "title": name,                # 元素中文名（点亮前隐藏，作为谜底）
            "chapter": chapter,
            "order": z,                   # 用真实原子序数排序，决定稳定的星点位置
            "summary": riddle,            # 谜面
            "keyPoints": [
                f"原子序数 {z}　元素符号 {symbol}",
                f"类别：{category}",
                f"用途/特征：{use}",
            ],
            "questions": [],              # 化学采用填化学式模式，不用选择题
            "symbol": symbol,             # 元素符号
            "formula": formula,           # 单质化学式（展示用）
            "explanation": explanation,   # 元素说明
        })

    return {"chapters": CHAPTERS, "knowledgePoints": points}


def main():
    here = os.path.dirname(os.path.abspath(__file__))
    out = os.path.normpath(os.path.join(here, "..", "public", "data", "chemistry-points.json"))
    data = build()
    with open(out, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"wrote {len(data['knowledgePoints'])} elements -> {out}")


if __name__ == "__main__":
    main()
